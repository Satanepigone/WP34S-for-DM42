void findlengths(unsigned short int posns[257], int smallp) {
	const int mask = smallp ? 256 : 0;
	int i;

	posns[0] = 0;
	for(i=0; i<256; i++)
		posns[i+1] = posns[i] + charlengths(i + mask) - 1;
}

